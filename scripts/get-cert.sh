#!/bin/bash

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ .env
if [ -f .env ]; then
  export $(grep -v '^#' .env | xargs)
